# Data Types Example

name = "Spurthi"      # String
age = 20              # Integer
height = 5.4          # Float
is_student = True     # Boolean

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

print("\nData Types:")
print(type(name))
print(type(age))
print(type(height))
print(type(is_student))